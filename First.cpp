#include<iostream>
using namespace std;

int main(){
    cout << "Hello World!";
    return 0;
}

/*
What is cout function?
cout stands for console output.
cout is a function that prints the content of the variable to the screen.

Syntax of cout function is as follows
cout << "Hello World!";

cout << a << "\n" << b << endl << c;


endl function is called end line.
endl is a function that prints the content of the variable to the screen and moves to the next line.

cin function is called console input.

cin >> a;   //inputs the value of a from the console.

cin >> a >> b; //inputs the value of a and b from the console.

*/